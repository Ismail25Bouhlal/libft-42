#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	int		i;
	char	*src;

	len = ft_strlen(s1);
	src = malloc(len * sizeof(char) + 1);
	if (src == 0)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		src[i] = s1[i];
		i++;
	}
	src[i] = '\0';
	return (src);
}
